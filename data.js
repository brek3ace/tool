$(document).ready(function () {
    var htmlTemplate =
        '<div class="container baithi white p-4">' +
        '<div class="cauhoi">' +
        '<div class="cauhoi_stt">Câu {questionNumber}</div>' +
        '<div class="clear"></div>' +
        '<div class="cauhoi_content">{questionContent}</div>' +
        "</div>" +
        '<div class="dapan">' +
        '<div class="row">{answers}</div>' +
        '<div class="clearfix"></div>' +
        "</div>" +
        '<div class=""></div></div>';

    var answerTemplate =
        '<div class="col-md-6 relative">' +
        '<div class="abcd">{optionChar}</div>' +
        '<div class="funkyradio">' +
        '<div class="funkyradio-primary">' +
        '<input type="radio" name="dapan{questionId}" id="{optionId}" />' +
        '<label for="{optionId}"> {optionContent} </label>' +
        "</div></div></div>";

    var container = document.querySelector(".container.baithi");
    var questionNumber = 1;

    data.forEach(function (item) {
        var questionContent = item.question;
        var answersHtml = "";

        item.answer.forEach(function (ans, idx) {
            var optionChar = String.fromCharCode(65 + idx);
            var optionId = "dapan" + (103134 + idx); // Đổi ID này cho mỗi câu trả lời
            var answerHtml = answerTemplate.replace("{optionChar}", optionChar).replace("{optionId}", optionId).replace("{optionContent}", ans).replace("{questionId}", "25811"); // Đổi ID này cho mỗi câu hỏi

            answersHtml += answerHtml;
        });

        var questionHtml = htmlTemplate.replace("{questionNumber}", questionNumber).replace("{questionContent}", questionContent).replace("{answers}", answersHtml);

        container.innerHTML += questionHtml;
        questionNumber++;
    });
});
